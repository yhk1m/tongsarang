function countMatches(text, keywords) {
  let count = 0;
  for (const kw of keywords) {
    try {
      if (new RegExp(kw).test(text)) count++;
    } catch {
      if (text.includes(kw)) count++;
    }
  }
  return count;
}

function assignCriteria(item, criteriaList) {
  const text = ((item['발문'] || '') + ' ' + (item['문항내용'] || '')).replace(/\r?\n/g, ' ');
  if (text.trim().length < 10) return null;

  let bestCode = null, bestScore = 0;
  for (const c of criteriaList) {
    const score = countMatches(text, c.kw);
    if (score > bestScore) {
      bestScore = score;
      bestCode = c.code;
    }
  }
  return bestScore > 0 ? bestCode : null;
}

