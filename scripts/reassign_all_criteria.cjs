const mappingsPath = path.join(__dirname, '..', 'public', 'data', 'default_mappings.json');
const mappings = JSON.parse(fs.readFileSync(mappingsPath, 'utf8'));

for (const [subjName, areaDefs] of Object.entries(subjectDefs)) {
  const dataPath = path.join(__dirname, '..', 'public', 'data', `${subjName}.json`);
  const data = JSON.parse(fs.readFileSync(dataPath, 'utf8'));

  console.log(`\n${'='.repeat(50)}`);
  console.log(`  ${subjName} 성취기준 재할당 (${data.length}문항)`);
  console.log('='.repeat(50));

  let byKeyword = 0, byDefault = 0, noArea = 0;

  data.forEach(item => {
    const area = item['대단원'];
    const standards = areaDefs[area];
    if (!standards) { noArea++; return; }

    const text = ((item['발문'] || '') + ' ' + (item['문항내용'] || '')).replace(/\r?\n/g, ' ');
    const { id, score } = assignBest(text, standards);

    if (id && score > 0) {
      item['성취기준'] = id;
      byKeyword++;
    } else {
      // 키워드 매칭 실패 시 해당 대단원의 첫 번째 성취기준 (position-based 아님)
      item['성취기준'] = standards[0].id;
      byDefault++;
    }
  });

  console.log(`  키워드 매칭: ${byKeyword}, 기본값: ${byDefault}, 대단원 없음: ${noArea}`);

  // 통계
  const stdStats = {};
  data.forEach(d => {
    if (d['성취기준']) stdStats[d['성취기준']] = (stdStats[d['성취기준']] || 0) + 1;
  });
  Object.keys(stdStats).sort().forEach(k => console.log(`    ${k}: ${stdStats[k]}`));

  // 저장
  fs.writeFileSync(dataPath, JSON.stringify(data, null, 2), 'utf8');
  console.log(`  ${subjName}.json 저장 완료`);

  // default_mappings 업데이트
  if (!mappings[subjName]) mappings[subjName] = {};
  let mapCount = 0;
  data.forEach(item => {
    if (item['성취기준']) {
      const key = `${item['학년도']}_${item['분류']}_${item['번호']}`;
      mappings[subjName][key] = item['성취기준'];
      mapCount++;
    }
  });
  console.log(`  매핑: ${mapCount}개`);
}

// 저장
