function assignBest(text, standards) {
  let best = null, bestScore = 0;
  for (const std of standards) {
    let score = 0;
    for (const kw of std.kw) {
      try { if (new RegExp(kw, 'i').test(text)) score++; } catch { if (text.includes(kw)) score++; }
    }
    if (score > bestScore) { bestScore = score; best = std.id; }
  }
  return { id: best, score: bestScore };
}

