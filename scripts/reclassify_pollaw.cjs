const data = JSON.parse(fs.readFileSync(dataPath, 'utf8'));

let chByKw = 0, chByNum = 0;
data.forEach(item => {
  const text = ((item['발문'] || '') + ' ' + (item['문항내용'] || '')).replace(/\r?\n/g, ' ');
  const { match, score } = bestMatch(text, chapters);
  if (match && score > 0) {
    item['대단원'] = match;
    chByKw++;
  } else {
    // 번호 기반 (6단원 포함)
    const num = parseInt(item['번호']);
    if (num <= 3) item['대단원'] = '1. 민주주의와 헌법';
    else if (num <= 6) item['대단원'] = '2. 민주 국가와 정부';
    else if (num <= 9) item['대단원'] = '3. 정치 과정과 참여';
    else if (num <= 13) item['대단원'] = '4. 개인 생활과 법';
    else if (num <= 18) item['대단원'] = '5. 사회생활과 법';
    else item['대단원'] = '6. 국제 관계와 한반도';
    chByNum++;
  }
});

