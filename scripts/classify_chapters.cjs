Object.keys(results).sort().forEach(key => {
  console.log(`\n--- ${key} ---`);
  results[key].forEach(r => {
    console.log(`  ${r.번호}번 → ${r.대단원} ${r.hint} (score: ${r.score})`);
  });
});

