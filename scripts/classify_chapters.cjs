// 문항 번호 기반 추론 보조 (한국지리 시험은 대체로 대단원 순서로 출제)
// 1~2번: 1단원, 3~7번: 2단원, 8~10번: 3단원, 11~13번: 4단원, 14~16번: 5단원, 17~18번: 6단원, 19~20번: 7단원
// 이건 보조적으로만 사용

function getTextScore(text, rule) {
  let score = 0;
  for (const kw of rule.keywords) {
    try {
      const regex = new RegExp(kw, 'i');
      if (regex.test(text)) {
        score += 1;
      }
    } catch (e) {
      if (text.includes(kw)) {
        score += 1;
      }
    }
  }
  // Anti-keywords reduce score
  if (rule.antiKeywords) {
    for (const ak of rule.antiKeywords) {
      try {
        const regex = new RegExp(ak, 'i');
        if (regex.test(text)) {
          score -= 0.5;
        }
      } catch (e) {
        if (text.includes(ak)) {
          score -= 0.5;
        }
      }
    }
  }
  return score;
}

function classifyItem(item) {
  const text = ((item['발문'] || '') + ' ' + (item['문항내용'] || '')).replace(/\r?\n/g, ' ');

  let bestChapter = null;
  let bestScore = 0;

  for (const rule of rules) {
    const score = getTextScore(text, rule);
    if (score > bestScore) {
      bestScore = score;
      bestChapter = rule.chapter;
    }
  }

  // If no keywords matched, use question number heuristic
  if (bestScore === 0) {
    const num = parseInt(item['번호']);
    if (num <= 2) bestChapter = '1. 국토 인식과 지리 정보';
    else if (num <= 7) bestChapter = '2. 지형 환경과 인간 생활';
    else if (num <= 10) bestChapter = '3. 기후 환경과 인간 생활';
    else if (num <= 13) bestChapter = '4. 거주 공간의 변화와 지역 개발';
    else if (num <= 16) bestChapter = '5. 생산과 소비의 공간';
    else if (num <= 18) bestChapter = '6. 인구 변화와 다문화 공간';
    else bestChapter = '7. 우리나라의 지역 이해';
  }

  return { chapter: bestChapter, score: bestScore };
}

// 대상: 2022 10월학평부터
function isTarget(item) {
  const year = parseInt(item['학년도']);
  const cls = item['분류'];
  if (year > 2022) return true;
  if (year === 2022 && cls === '10월학평') return true;
  return false;
}

// 대단원이 비어있고 대상 시험인 문항만 분류
let classified = 0;
let byNumber = 0;
const results = {};

data.forEach((item, idx) => {
  if ((item['대단원'] === '' || !item['대단원']) && isTarget(item)) {
    const { chapter, score } = classifyItem(item);
    item['대단원'] = chapter;
    classified++;
    if (score === 0) byNumber++;

    const key = item['학년도'] + ' ' + item['분류'];
    if (!results[key]) results[key] = [];
    results[key].push({
      번호: item['번호'],
      대단원: chapter,
      score: score,
      hint: score === 0 ? '(번호추론)' : '',
    });
  }
});

